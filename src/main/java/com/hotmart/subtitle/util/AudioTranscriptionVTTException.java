package com.hotmart.subtitle.util;

public class AudioTranscriptionVTTException extends Exception {

	private static final long serialVersionUID = 7993347264979554017L;

	public AudioTranscriptionVTTException() {
		super();
	}

	public AudioTranscriptionVTTException(String message, Throwable cause) {
		super(message, cause);
	}

	public AudioTranscriptionVTTException(String message) {
		super(message);
	}

	public AudioTranscriptionVTTException(Throwable cause) {
		super(cause);
	}

}
